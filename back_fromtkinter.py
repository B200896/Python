from tkinter import *
t=Tk()
t.geometry("425x280")
def login():
    f2=Frame()
    f2.place(x=0,y=0,width=425,height=280)
    u=Label(f2,text="Login Page")
    u.place(x=150,
            y=10)
    u1=Label(f2,text="Enter Name")
    u1.place(x=100,y=80)
    e1=Entry(f2,font=("",12))
    e1.place(x=200,y=80,width=120)
    u2=Label(f2,text="Enter Pass")
    u2.place(x=100,y=130)
    e2=Entry(f2,font=("",12),show='*')
    e2.place(x=200,y=130,width=120)
    b2=Button(f2,text="back",command=home)
    b2.place(x=2,y=3)
    b3=Button(f2,text="Login")
    b3.place(x=150,y=200,width=120)
def regis():
    f3=Frame()
    f3.place(x=0,y=0,width=425,height=280)
    u=Label(f3,text="Registration Page")
    u.place(x=200,y=10)
    u1=Label(f3,text="Enter Student ID")
    u1.place(x=100,y=80)
    e1=Entry(f3,font=("",12))
    e1.place(x=250,y=80,width=120)
    u2=Label(f3,text="Enter Student Name")
    u2.place(x=100,y=130)
    e2=Entry(f3,font=("",12),show='*')
    e2.place(x=250,y=130,width=120)
    u3=Label(f3,text="Enter Student Class")
    u3.place(x=100,y=180)
    e3=Entry(f3,font=("",12),show='*')
    e3.place(x=250,y=180,width=120)   
   
    b2=Button(f3,text="Back",command=home)
    b2.place(x=2,y=3)
    b3=Button(f3,text="Regis")
    b3.place(x=250,y=250,width=120)
def home( ):    
    f1=Frame(bg="cyan")
    f1.place(x=0,y=0,width=425,height=280)
    b1=Button(f1,text="Login",command=login)
    b1.pack()
    b2=Button(f1,text="RIGS",command=regis)
    b2.pack()
home()
t.mainloop()
